
function changeBackground(x){
  switch(x){
    case 0:
      document.getElementById("calc").style.backgroundColor="#85FFF9";
      break;
    case 1:
      document.getElementById("calc").style.backgroundColor="#d9b3ff";
      break;
    case 2:
      document.getElementById("calc").style.backgroundColor="#ffff99";
      break;
    case 3:
      document.getElementById("calc").style.backgroundColor="#ff9999";
      break;
  }
}

//Drag part


var dragItem = document.querySelector("#calc");
var dragItem1 = document.querySelector("#interface");
var dragItem2 = document.querySelector("#buttons");
var container = document.querySelector("#container");

var active = false;
var currentX;
var currentY;
var initialX;
var initialY;
var xOffset = 0;
var yOffset = 0;

container.addEventListener("touchstart", dragStart, false);
container.addEventListener("touchend", dragEnd, false);
container.addEventListener("touchmove", drag, false);

container.addEventListener("mousedown", dragStart, false);
container.addEventListener("mouseup", dragEnd, false);
container.addEventListener("mousemove", drag, false);

function dragStart(e) {
  if (e.type === "touchstart") {
    initialX = e.touches[0].clientX - xOffset;
    initialY = e.touches[0].clientY - yOffset;
  } else {
    initialX = e.clientX - xOffset;
    initialY = e.clientY - yOffset;
  }

  if (e.target === dragItem||e.target === dragItem1||e.target === dragItem2) {
    active = true;
  }
}

function dragEnd(e) {
  initialX = currentX;
  initialY = currentY;

  active = false;
}

function drag(e) {
  if (active) {

    e.preventDefault();

    if (e.type === "touchmove") {
      currentX = e.touches[0].clientX - initialX;
      currentY = e.touches[0].clientY - initialY;
    } else {
      currentX = e.clientX - initialX;
      currentY = e.clientY - initialY;
    }

    xOffset = currentX;
    yOffset = currentY;

    setTranslate(currentX, currentY, dragItem);
  }
}

function setTranslate(xPos, yPos, el) {
  el.style.transform = "translate3d(" + xPos + "px, " + yPos + "px, 0)";
}
